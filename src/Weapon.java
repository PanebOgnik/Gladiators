public class Weapon {
    String name;
    int damage;
    int attackSpeed;
    int level;
    int price;

    public Weapon(String name, int damage, int attackSpeed, int level, int price) {
        this.name = name;
        this.damage = damage;
        this.attackSpeed = attackSpeed;
        this.level = level;
        this.price = price;
    }
}
